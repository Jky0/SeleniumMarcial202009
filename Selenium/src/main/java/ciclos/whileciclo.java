package ciclos;  

public class whileciclo {

	public static void main(String[] args) {
		
		int a=1;
		int b=2;
		
		//Salir cuando "a" es menor o igual que 4
		while (a <= 50 && b==2 ) 
		{
			System.out.println("el valor de A: "+ a +" valor" + b);
			//Incrementar el valor de "a" para la siguiente iteracion 
			a++;
			b++;
		}

	}

}
